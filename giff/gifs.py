from tkinter import *
root = Tk()

frameCnt = 13
frames = [PhotoImage(file='C:\\Users\\mui\\Desktop\\giff\\208803700.gif',format = 'gif -index %i' %(i)) for i in range(frameCnt)]

def update(ind):

    frame = frames[ind]
    ind += 1
    if ind == frameCnt:
        ind = 0
    label.configure(image=frame)
    root.after(100, update, ind)
label = Label(root)
label.pack()
root.after(0, update, 0)
root.mainloop()